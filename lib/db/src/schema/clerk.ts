// ============ Clerk v0 — claims register (C0) ============
// A ClaimRecord is the ONLY source of binding compliance facts the Clerk may
// surface (rates, thresholds, deadlines, citations). Facts are inserted into
// answers verbatim by application code — never generated by a model. Records
// are versioned per claimKey; the maker-checker rule (author of a version can
// never approve it) is enforced at the route layer, and "at most one active
// version per claimKey" is a DB invariant via a partial unique index.

export const claimStateEnum = pgEnum("claim_state", [
  "draft",
  "review",
  "active",
  "suspended",
  "superseded",
  "expired",
  "rejected",
]);

// A typed, protected fact. `value` is stored as text to keep exact-decimal
// fidelity (e.g. "7.5", "50000", "24"); `kind` drives client-side formatting.
export interface ProtectedFact {
  key: string;
  label: string;
  kind: "rate" | "amount" | "duration" | "date" | "count" | "text";
  value: string;
  unit?: string;
}

// Deterministic applicability filters evaluated in app code (never the model).
export interface ClaimApplicability {
  category?: "b2b" | "b2g" | "b2c";
  taxpayerSize?: "small" | "medium" | "large";
  [key: string]: string | undefined;
}

export const claimRecordsTable = pgTable(
  "claim_records",
  {
    id: id(),
    // Stable logical identifier, e.g. "vat.standard_rate". Versions share it.
    claimKey: text("claim_key").notNull(),
    version: integer("version").notNull(),
    state: claimStateEnum("state").notNull().default("draft"),
    // Human-readable one-line summary of what the claim asserts.
    title: text("title").notNull(),
    // The full plain-language proposition shown to operators and used as the
    // fixed answer template. Facts are substituted from protectedFacts only.
    proposition: text("proposition").notNull(),
    protectedFacts: jsonb("protected_facts")
      .$type<ProtectedFact[]>()
      .notNull()
      .default([]),
    // Statutory citation, e.g. "VAT Act s.4 (as amended by Finance Act 2020)".
    citation: text("citation").notNull(),
    applicability: jsonb("applicability")
      .$type<ClaimApplicability>()
      .notNull()
      .default({}),
    effectiveFrom: date("effective_from").notNull(),
    effectiveTo: date("effective_to"),
    // Governance freshness (CLK-KB-07): an active claim whose review date has
    // passed is still visible in the register but can no longer answer.
    reviewDueAt: date("review_due_at"),
    // Maker-checker lineage: the author (maker) of this version, who moved it
    // to review, and who approved/rejected it (checker; never the author).
    createdBy: uuid("created_by")
      .notNull()
      .references(() => usersTable.id),
    submittedBy: uuid("submitted_by").references(() => usersTable.id),
    decidedBy: uuid("decided_by").references(() => usersTable.id),
    decisionNote: text("decision_note"),
    // Set on the OLD version when a newer version becomes active.
    supersededById: uuid("superseded_by_id"),
    createdAt: createdAt(),
    updatedAt: updatedAt(),
  },
  (t) => [
    unique().on(t.claimKey, t.version),
    // DB invariant: at most one ACTIVE version per logical claim.
    uniqueIndex("claim_records_one_active_per_key")
      .on(t.claimKey)
      .where(sql`${t.state} = 'active'`),
  ],
);

// ============ Clerk v0 — capture cases and Q&A (C1) ============
// Cross-tenant operator work-in-progress. Holds untrusted uploaded document
// content and unreviewed model output, so it is RLS'd bypass-only (operators
// and auditors run with app.bypass='on'; no firm-scoped principal can reach
// these rows) — see migration 0005.

export const clerkCaseKindEnum = pgEnum("clerk_case_kind", [
  "extraction",
  "question",
]);

export const clerkCaseStatusEnum = pgEnum("clerk_case_status", [
  "pending",
  "extracted",
  "in_review",
  "approved",
  "rejected",
  "escalated",
  "failed",
]);

export const clerkSourceTypeEnum = pgEnum("clerk_source_type", [
  "image",
  "pdf",
  "text",
  // Voice notes are transcribed on intake; only the transcript is retained
  // (sourceText) — raw audio is never stored (OPEN-8 minimisation posture).
  "voice",
]);

// One extraction candidate per canonical invoice field. `critical` fields
// (party identity, TINs, invoice number/date, currency, totals, tax) are
// ALWAYS flagged for human confirmation regardless of confidence.
export interface ExtractionField {
  field: string;
  value: string | null;
  confidence: number;
  sourceSnippet: string | null;
  critical: boolean;
  flagged: boolean;
}

export interface ExtractionLine {
  description: string | null;
  quantity: string | null;
  unitPrice: string | null;
  vatRate: string | null;
  confidence: number;
}

export interface ClerkExtraction {
  fields: ExtractionField[];
  lines: ExtractionLine[];
  promptVersion: string;
  model: string;
}

// Deterministic pre-approval checks run over the extraction (never the model):
// missing critical values, malformed dates/rates, totals that don't add up.
// Empty array = nothing blocking; null = extraction never succeeded.
export interface PreflightIssue {
  field: string;
  message: string;
}

// Per-field record of what the operator changed between the model's proposal
// and the approved values — the labeled-outcome exhaust the two-layer thesis
// runs on (every extraction a human corrects is training signal for later
// phases, and the honest measure of extraction quality today).
export interface ClerkCorrection {
  field: string;
  extracted: string | null;
  final: string | null;
  changed: boolean;
}

// Assembled deterministically from an approved ClaimRecord (answered=true) or
// a neutral refusal (answered=false). Never free model prose.
export interface ClerkAnswer {
  answered: boolean;
  claimId?: string;
  claimKey?: string;
  claimVersion?: number;
  proposition?: string;
  facts?: ProtectedFact[];
  citation?: string;
  refusalReason?: string;
}

export const clerkCasesTable = pgTable("clerk_cases", {
  id: id(),
  kind: clerkCaseKindEnum("kind").notNull(),
  status: clerkCaseStatusEnum("status").notNull().default("pending"),
  // --- extraction cases ---
  sourceType: clerkSourceTypeEnum("source_type"),
  sourceName: text("source_name"),
  // Extracted/uploaded text (pdf/text sources). Untrusted document content.
  sourceText: text("source_text"),
  // Base64 image payload (image sources). Excluded from list endpoints.
  sourceImageB64: text("source_image_b64"),
  // sha256 of the exact source content (text/transcript/image bytes) —
  // duplicate-intake detection without retaining a second copy.
  sourceHash: text("source_hash"),
  // Recorder-reported length of a voice note, in seconds (voice sources only).
  sourceDurationSec: integer("source_duration_sec"),
  extraction: jsonb("extraction").$type<ClerkExtraction>(),
  // Recomputed on every successful (re-)extraction; see modules/clerk/preflight.
  preflight: jsonb("preflight").$type<PreflightIssue[]>(),
  // --- question cases ---
  question: text("question"),
  answer: jsonb("answer").$type<ClerkAnswer>(),
  // --- review claim (one operator actively works a case at a time) ---
  claimedBy: uuid("claimed_by").references(() => usersTable.id),
  claimedAt: timestamp("claimed_at", { withTimezone: true }),
  // --- review decision ---
  firmId: uuid("firm_id").references(() => firmsTable.id),
  createdBy: uuid("created_by")
    .notNull()
    .references(() => usersTable.id),
  decidedBy: uuid("decided_by").references(() => usersTable.id),
  decisionAction: text("decision_action"),
  decisionReason: text("decision_reason"),
  // Extraction-vs-approved diff, computed at approval (see computeCorrections).
  corrections: jsonb("corrections").$type<ClerkCorrection[] | null>(),
  createdInvoiceId: uuid("created_invoice_id").references(
    () => invoicesTable.id,
  ),
  failReason: text("fail_reason"),
  createdAt: createdAt(),
  updatedAt: updatedAt(),
},
// The duplicate-intake guard probes source_hash on every case creation; the
// queue lists filter by status.
(t) => [
  index("clerk_cases_source_hash_idx").on(t.sourceHash),
  index("clerk_cases_status_idx").on(t.status),
]);

// Append-only ledger of every model invocation (enforced by trigger in
// migration 0005): model, prompt version, input reference (hash — never the
// raw document), typed output and the decision taken on it.
export const clerkInferenceOutcomeEnum = pgEnum("clerk_inference_outcome", [
  "ok",
  "invalid_discarded",
  "killed",
  "error",
]);

export const clerkInferenceCallsTable = pgTable("clerk_inference_calls", {
  id: id(),
  caseId: uuid("case_id").references(() => clerkCasesTable.id),
  // The firm the call was made on behalf of (client capture, firm Ask Clerk).
  // Null for operator/platform traffic. Drives the per-firm monthly token
  // budget and lets usage be read under the firm-keyed RLS policy (0009).
  firmId: uuid("firm_id").references(() => firmsTable.id),
  purpose: text("purpose").notNull(),
  model: text("model").notNull(),
  promptVersion: text("prompt_version").notNull(),
  // sha256 of the exact input document/question — auditable without retaining
  // a second copy of untrusted content in the ledger.
  inputRef: text("input_ref").notNull(),
  outputJson: jsonb("output_json").$type<unknown>(),
  schemaValid: boolean("schema_valid").notNull(),
  outcome: clerkInferenceOutcomeEnum("outcome").notNull(),
  errorText: text("error_text"),
  latencyMs: integer("latency_ms"),
  // Token usage where the provider reports it (CLK-NFR-04 cost-to-serve).
  promptTokens: integer("prompt_tokens"),
  completionTokens: integer("completion_tokens"),
  createdAt: createdAt(),
},
// The watchdog and metrics scan recent windows; case joins walk case_id; the
// per-firm budget sums a firm's month-to-date tokens on every capture/ask.
(t) => [
  index("clerk_inference_calls_created_idx").on(t.createdAt),
  index("clerk_inference_calls_case_idx").on(t.caseId),
  index("clerk_inference_calls_firm_idx").on(t.firmId, t.createdAt),
]);

// One fixture's outcome inside an evaluation run (§13.1). Expected/actual
// values are synthetic C1 fixture data, never client content.
export interface ClerkEvalFixtureResult {
  key: string;
  label: string;
  riskLabel: "clean" | "skewed" | "injection" | "correction";
  outcome: "ok" | "invalid" | "error";
  fieldsCompared: number;
  fieldsCorrect: number;
  mismatches: { field: string; expected: string | null; actual: string | null }[];
  // Injection fixtures only: true when every critical field still matched the
  // legitimate document values (the planted instruction changed nothing).
  injectionResisted: boolean | null;
}

// Evaluation runs (§13.1): an operator-triggered pass of the synthetic fixture
// corpus through the LIVE gateway, scored per field against expected values.
// The regression evidence the monthly Readiness Review wants — and the early
// warning when a prompt or model change degrades extraction before operators
// feel it. Append-only evidence (trigger in migration 0006).
export const clerkEvalRunsTable = pgTable("clerk_eval_runs", {
  id: id(),
  // Null for runs started by the nightly learning-loop sweep (no human actor).
  startedBy: uuid("started_by").references(() => usersTable.id),
  model: text("model").notNull(),
  promptVersion: text("prompt_version").notNull(),
  fixtureCount: integer("fixture_count").notNull(),
  fieldsCompared: integer("fields_compared").notNull(),
  fieldsCorrect: integer("fields_correct").notNull(),
  injectionFixtures: integer("injection_fixtures").notNull(),
  injectionResisted: integer("injection_resisted").notNull(),
  results: jsonb("results").$type<ClerkEvalFixtureResult[]>().notNull(),
  durationMs: integer("duration_ms").notNull(),
  createdAt: createdAt(),
});

// Learning loop (Clerk expansion B): every human-corrected approval becomes a
// ground-truth eval fixture — the sweep grows this corpus from the correction
// exhaust, and every eval run (manual or nightly) scores against the static
// corpus PLUS these. One fixture per case; content is the case's fenced
// sourceText and the operator-approved field values.
export const clerkEvalFixturesTable = pgTable("clerk_eval_fixtures", {
  id: id(),
  caseId: uuid("case_id")
    .notNull()
    .unique()
    .references(() => clerkCasesTable.id),
  label: text("label").notNull(),
  sourceText: text("source_text").notNull(),
  expected: jsonb("expected").$type<Record<string, string | null>>().notNull(),
  createdAt: createdAt(),
});

// ============ Clerk power D — weekly firm digest ============
// One row per firm per ISO week. The FACTS in a digest are computed by SQL
// (deadlines, failures, receivables); the model only phrases them — and when
// it can't (kill switch, budget, invalid output), the deterministic template
// text is stored instead, recorded via `source`. Firm-keyed RLS (migration
// 0011) so a firm reads only its own digests.

export const clerkDigestSourceEnum = pgEnum("clerk_digest_source", [
  "clerk",
  "template",
]);

export const clerkDigestsTable = pgTable(
  "clerk_digests",
  {
    id: id(),
    firmId: uuid("firm_id")
      .notNull()
      .references(() => firmsTable.id),
    // Monday 00:00 UTC of the week the digest covers.
    weekStart: timestamp("week_start", { withTimezone: true }).notNull(),
    headline: text("headline").notNull(),
    bullets: jsonb("bullets").$type<string[]>().notNull().default([]),
    source: clerkDigestSourceEnum("source").notNull(),
    createdAt: createdAt(),
  },
  // The sweep's idempotency anchor: at most one digest per firm per week.
  (t) => [unique().on(t.firmId, t.weekStart)],
);
export type ClerkDigestRow = typeof clerkDigestsTable.$inferSelect;

export type ClaimRecord = typeof claimRecordsTable.$inferSelect;
export type ClerkCase = typeof clerkCasesTable.$inferSelect;
export type ClerkEvalRun = typeof clerkEvalRunsTable.$inferSelect;
